using System;

class FloatOrDouble
{
    static void Main()
    {
        double ldFirstVar = 34.567839023;
        float lfSecVar = 12.345F;
        double ldThirdVar = 8923.1234857;
        float lfFourthVar = 3456.091F;
        Console.WriteLine("My first var is double : {0}", ldFirstVar);
        Console.WriteLine("My second var is float : {0}", lfSecVar);
        Console.WriteLine("My third var is double : {0}", ldThirdVar);
        Console.WriteLine("My fourth var is float : {0}", lfFourthVar);
    }
}
