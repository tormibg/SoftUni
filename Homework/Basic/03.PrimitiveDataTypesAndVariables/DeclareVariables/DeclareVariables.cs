using System;

class DeclareVariables
{
    static void Main()
    {
        ushort lusFirstVar = 52130;
        SByte lsbSecVar = -115;
        int liThirdVar = 4825932;
        SByte lsbFourthVar = 97;
        short lsFifthVar = -10000;
        Console.WriteLine("My first var is ushort : {0}",lusFirstVar);
        Console.WriteLine("My second var is sbyte : {0}",lsbSecVar);
        Console.WriteLine("My third var is int : {0}",liThirdVar);
        Console.WriteLine("My fourth var is sbyte : {0}",lsbFourthVar);
        Console.WriteLine("My fifth var is short : {0}",lsFifthVar);
    }
}
